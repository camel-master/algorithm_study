"""
BOJ 2869 달팽이는 올라가고 싶다
https://www.acmicpc.net/problem/2869

하루 동안 A 미터를 올라가고 B미터를 미끄러진다.
높이 V에 도달 하기 까지 며칠이 걸리는가?

1 ≤ B < A ≤ V ≤ 1,000,000,000

2 - 1 = 1 => 5 / 1 = 5.0
ceil((5 - 2) / (2 - 1)) + 1
5 - 1 = 4 => 6 / 4 = 1.5
100 - 99 = 1 => 1,000,000,000 / 1 = 1000000000
ceil((1,000,000,000 - 100) / 1) + 1
999,999,901
"""
import math

A, B, V = map(int, input().split())
print(math.ceil((V - A) / (A - B)) + 1)
